// Helper function to get tarot card meaning
function getTarotMeaning(cardName) {
    const cards = fortuneTellingKnowledge.tarot.majorArcana;
    for (const [key, card] of Object.entries(cards)) {
        if (card.name.toLowerCase().includes(cardName.toLowerCase())) {
            return card;
        }
    }
    return null;
}

// Helper function to get numerology meaning
function getNumerologyMeaning(number) {
    return fortuneTellingKnowledge.numerology[number] || null;
}

